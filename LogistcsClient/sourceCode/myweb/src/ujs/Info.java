package ujs;

public class Info {
	String id;
	String password;
	int gid;
	
	public Info()
	{
		
	}
	public String getId()
	{
		return id;
	}
	public void setId(String newId)
	{
		id=newId;
	}
	public String getPassword()
	{
		return password;
	}
	public void setPassword(String newPassword)
	{
		password=newPassword;
	}
	public int getGid()
	{
		return gid;
	}
	public void setGid(int newGid)
	{
		gid=newGid;
	}
}
